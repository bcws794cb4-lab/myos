// Mode 13h: 320x200, 256 colors
// Framebuffer at 0xA0000, 1 byte per pixel

#define SCREEN_WIDTH  320
#define SCREEN_HEIGHT 200
#define FRAMEBUFFER   0xA0000

typedef unsigned char uint8_t;

// Mode 13h 256-color palette indices
#define BLACK   0
#define BLUE    1
#define GREEN   2
#define CYAN    3
#define RED     4
#define WHITE   15
#define GRAY    8
#define LBLUE   9
#define LGREEN  10
#define LRED    12
#define YELLOW  14
#define DBLUE   17
#define DKBLUE  19

void put_pixel(int x, int y, uint8_t color) {
    if (x < 0 || x >= SCREEN_WIDTH || y < 0 || y >= SCREEN_HEIGHT) return;
    uint8_t *fb = (uint8_t *)FRAMEBUFFER;
    fb[y * SCREEN_WIDTH + x] = color;
}

void fill_rect(int x, int y, int w, int h, uint8_t color) {
    for (int row = y; row < y + h; row++)
        for (int col = x; col < x + w; col++)
            put_pixel(col, row, color);
}

void draw_hline(int x, int y, int w, uint8_t color) {
    for (int i = x; i < x + w; i++) put_pixel(i, y, color);
}

void draw_vline(int x, int y, int h, uint8_t color) {
    for (int i = y; i < y + h; i++) put_pixel(x, i, color);
}

void draw_rect(int x, int y, int w, int h, uint8_t color) {
    draw_hline(x, y, w, color);
    draw_hline(x, y + h - 1, w, color);
    draw_vline(x, y, h, color);
    draw_vline(x + w - 1, y, h, color);
}

