void draw_char(int x, int y, char c, uint8_t color, uint8_t bg) {
    if (c < 32 || c > 127) return;
    const uint8_t *glyph = font[c - 32];
    for (int row = 0; row < 8; row++)
        for (int col = 0; col < 8; col++)
            put_pixel(x + col, y + row,
                (glyph[row] & (0x80 >> col)) ? color : bg);
}

void draw_string(int x, int y, const char *str, uint8_t color, uint8_t bg) {
    int i = 0;
    while (str[i]) { draw_char(x + i * 6, y, str[i], color, bg); i++; }
}

